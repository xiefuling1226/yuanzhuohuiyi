function buildCelebrityDatabaseSummary() {
  if (typeof DOMAINS === 'undefined' || typeof CELEBRITIES === 'undefined') return '';
  const lines = [];
  for (const domain of DOMAINS) {
    const items = domain.members
      .map(key => {
        const c = CELEBRITIES[key];
        if (!c) return null;
        const desc = c.description || '';
        const expertMatch = desc.match(/精通([^。]+)/);
        const keywords = expertMatch ? expertMatch[1].replace(/[、，]/g, '/').slice(0, 30) : '';
        return keywords ? `${c.displayName}(${keywords})` : `${c.displayName}(${c.title})`;
      })
      .filter(Boolean);
    lines.push(`■ ${domain.name}(${items.length}位)：${items.join('、')}`);
  }
  return lines.join('\n');
}

